var num1 = 10;
var num2 = 5;

num1 = num1 + 1; //num1 변수의 값은 num1변수의 값인 10 + 1이 된다.
document.write(num1 + "<br>");

num2 = num2 + 5; //num2 변수의 값은 num2변수의 값인 5 + 5가 된다.
document.write(num2 + "<br>");

num2 = num2 / 2;
document.write(num2 + "<br>");

